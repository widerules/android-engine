package org.chemodansama.engine.render.imgui;

public class NpGuiRect {
    
    private float mX;
    private float mY;
    private float mW;
    private float mH;
    
    public NpGuiRect() {
        mX = 0;
        mY = 0;
        mW = 0;
        mH = 0;
    }
    
    public float getX() {
        return mX;
    }
    
    public float getY() {
        return mY;
    }
    
    public float getW() {
        return mW;
    }
    
    public float getH() {
        return mH;
    }
}
